#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t l;

	l = ft_strlen(s1);
	while (*s2 != '\0')
	{
		s1[l] = *s2;
		l++;
		s2++;
	}
	s1[l] = '\0';
	return (s1);
}
