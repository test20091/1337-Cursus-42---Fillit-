#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*str;

	if (s != NULL)
	{
		str = (char*)s;
		while (*str)
		{
			ft_putchar_fd(*str, fd);
			str++;
		}
	}
}
